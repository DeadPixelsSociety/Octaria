/// retourne norme du vecteur AB
//--------------------------------------------------------------------------------------------------
/*virtual*/ float CGameState::computeVecteurNorme(float Ax, float Ay, float Bx, float By)
{
	return sqrt(((Bx-Ax)*(Bx-Ax)) + ((By-Ay)*(By-Ay)));
}