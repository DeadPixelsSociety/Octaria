//--------------------------------------------------------------------------------------------------
/// @todo comment
//--------------------------------------------------------------------------------------------------
void CGamePlayer::Release(void)
{
	int nPlayerCount = m_aPoulpeList.GetCount();
	for (int i = 0; i < nPlayerCount; ++i)
	{
		m_aPoulpeList[i]->Release();
		SH_SAFE_DELETE(m_aPoulpeList[i]);
	}
	m_aPoulpeList.Empty();

	m_aInventory.Empty();
}

