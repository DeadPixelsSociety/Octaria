void CGamePlayer::Release(void)
{
	int nPlayerCount = m_aPlayerList.GetCount();
	for (int i = 0; i < nPlayerCount; ++i)
	{
		m_aPlayerList[i] = shNULL;
	}
	m_aPlayerList.Empty();

	m_aInventory.Empty();
}

